    private void preencherCombos() {
        comboFilme.removeAllItems();
        comboSala.removeAllItems();
        for (Filme f : GerenciamentoFilmes.listaFilmes) {
            comboFilme.addItem(f.getTitulo());
        }
        for (Sala s : GerenciamentoSalas.listaSalas) {
            comboSala.addItem(String.valueOf(s.getNumero()));
        }
    }

