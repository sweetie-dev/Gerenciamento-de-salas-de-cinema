    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String horario = txtHorario.getText().trim();
        if (horario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o horário da sessão.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idxFilme = comboFilme.getSelectedIndex();
        int idxSala = comboSala.getSelectedIndex();

        if (idxFilme < 0 || idxFilme >= GerenciamentoFilmes.listaFilmes.size()) {
            JOptionPane.showMessageDialog(this, "Selecione um filme válido.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (idxSala < 0 || idxSala >= GerenciamentoSalas.listaSalas.size()) {
            JOptionPane.showMessageDialog(this, "Selecione uma sala válida.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Filme filme = GerenciamentoFilmes.listaFilmes.get(idxFilme);
        Sala sala = GerenciamentoSalas.listaSalas.get(idxSala);

        Sessao sessao = new Sessao(horario, filme, sala);
        GerenciamentoSessoes.listaSessoes.add(sessao);
        // Atualiza tabela do gerenciador
        GerenciamentoSessoes ger = new GerenciamentoSessoes();
        ger.atualizarTabelaSessoes();

        JOptionPane.showMessageDialog(this, "Sessão adicionada com sucesso!");
    }//GEN-LAST:event_btnSalvarActionPerformed

